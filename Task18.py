"""Проверить, является ли натуральное число степенью двойки."""


def numbers():
    number = input('Введіть число для перевірки, що це 2 в якісь степені: ')
    number = int(number)
    if number&(number-1)==0:
        print('Це число є степенью двійки.')
    else:
        print('Це число не є степенью двійки.')

if __name__ == '__main__':
    numbers()
